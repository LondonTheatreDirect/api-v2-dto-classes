package LTD_API;

public class VenueInfo
{
    private int venueId;
    private String name;
    private String info;
    private String address;
    private String city;
    private String postcode;
    private String telephone;
    private String fax;
    private String email;
    private String nearestTube;
    private String train;
    private String planLink;
    private String imageUrl;
    
    public int getVenueId()
    {
        return venueId;
    }
    public void setVenueId(int venueId)
    {
        this.venueId = venueId;
    }

    public String getName()
    {
        return name;
    }
    public void setName(String name)
    {
        this.name = name;
    }

    public String getInfo()
    {
        return info;
    }
    public void setInfo(String info)
    {
        this.info = info;
    }

    public String getAddress()
    {
        return address;
    }
    public void setAddress(String address)
    {
        this.address = address;
    }

    public String getCity()
    {
        return city;
    }
    public void setCity(String city)
    {
        this.city = city;
    }

    public String getPostcode()
    {
        return postcode;
    }
    public void setPostcode(String postcode)
    {
        this.postcode = postcode;
    }

    public String getTelephone()
    {
        return telephone;
    }
    public void setTelephone(String telephone)
    {
        this.telephone = telephone;
    }

    public String getFax()
    {
        return fax;
    }
    public void setFax(String fax)
    {
        this.fax = fax;
    }

    public String getEmail()
    {
        return email;
    }
    public void setEmail(String email)
    {
        this.email = email;
    }

    public String getNearestTube()
    {
        return nearestTube;
    }
    public void setNearestTube(String nearestTube)
    {
        this.nearestTube = nearestTube;
    }

    public String getTrain()
    {
        return train;
    }
    public void setTrain(String train)
    {
        this.train = train;
    }

    public String getPlanLink()
    {
        return planLink;
    }
    public void setPlanLink(String planLink)
    {
        this.planLink = planLink;
    }

    public String getImageUrl()
    {
        return imageUrl;
    }
    public void setImageUrl(String imageUrl)
    {
        this.imageUrl = imageUrl;
    }
}

